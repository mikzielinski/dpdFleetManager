/**
 * Embeds Roboto latin-ext (PL diacritics) for jsPDF.
 * Run: node scripts/generate-pdf-fonts.mjs
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');
const out = path.join(root, 'src/services/pdfFonts.generated.ts');

const sources = [
  {
    url: 'https://cdn.jsdelivr.net/fontsource/fonts/roboto@latest/latin-ext-400-normal.ttf',
    vfs: 'Roboto-Regular.ttf',
    family: 'Roboto',
    style: 'normal',
  },
  {
    url: 'https://cdn.jsdelivr.net/fontsource/fonts/roboto@latest/latin-ext-700-normal.ttf',
    vfs: 'Roboto-Bold.ttf',
    family: 'Roboto',
    style: 'bold',
  },
];

const fonts = [];
for (const src of sources) {
  const res = await fetch(src.url);
  if (!res.ok) throw new Error(`Failed to fetch ${src.url}: ${res.status}`);
  const buf = Buffer.from(await res.arrayBuffer());
  fonts.push({ ...src, base64: buf.toString('base64') });
}

const body = `/** Generated by scripts/generate-pdf-fonts.mjs — do not edit manually. */
export const PDF_FONT_ENTRIES = ${JSON.stringify(fonts.map(({ vfs, family, style, base64 }) => ({ vfs, family, style, base64 })), null, 2)} as const;
`;

fs.writeFileSync(out, body);
console.log(`Wrote ${out} (${fonts.length} fonts)`);
